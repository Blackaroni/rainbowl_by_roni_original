DROP TRIGGER IF EXISTS trg_rainbowl_customers_updated_at ON rainbowl.customers;
CREATE TRIGGER trg_rainbowl_customers_updated_at
BEFORE UPDATE ON rainbowl.customers
FOR EACH ROW
EXECUTE FUNCTION rainbowl.set_updated_at();

DROP TRIGGER IF EXISTS trg_rainbowl_products_updated_at ON rainbowl.products;
CREATE TRIGGER trg_rainbowl_products_updated_at
BEFORE UPDATE ON rainbowl.products
FOR EACH ROW
EXECUTE FUNCTION rainbowl.set_updated_at();

DROP TRIGGER IF EXISTS trg_rainbowl_orders_updated_at ON rainbowl.orders;
CREATE TRIGGER trg_rainbowl_orders_updated_at
BEFORE UPDATE ON rainbowl.orders
FOR EACH ROW
EXECUTE FUNCTION rainbowl.set_updated_at();

CREATE OR REPLACE VIEW rainbowl.sales_lines AS
SELECT
    orders.order_date AS date,
    COALESCE(products.legacy_product_id, products.id::TEXT) AS product_id,
    products.name AS product_name,
    COALESCE(customers.legacy_customer_id, customers.id::TEXT) AS customer_id,
    customers.name AS customer_name,
    order_items.quantity AS quantity,
    order_items.unit_cost AS cost_price,
    order_items.unit_price AS selling_price,
    order_items.discount_amount AS discount,
    order_items.line_margin AS margin,
    orders.payment_status AS payment_status,
    orders.fulfillment_status AS fulfillment_status
FROM rainbowl.order_items AS order_items
JOIN rainbowl.orders AS orders ON orders.id = order_items.order_id
JOIN rainbowl.customers AS customers ON customers.id = orders.customer_id
JOIN rainbowl.products AS products ON products.id = order_items.product_id;
